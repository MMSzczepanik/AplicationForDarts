    private void init(){
        try {
            Class.forName("org.sqlite.JDBC");
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(TournamentApp.class.getName()).log(Level.SEVERE, null, ex);
        }
        ConnectionSource connectionSource;
        try {
            connectionSource = new JdbcConnectionSource(TournamentApp.DB_NAME);
            Dao<Player, String> playerDao = DaoManager.createDao(connectionSource, Player.class);
            TableUtils.createTableIfNotExists(connectionSource, Player.class);
            
            QueryBuilder<Player, String> queryBuilder = playerDao.queryBuilder();
            List<Player> allPlayerList = playerDao.queryForAll();
            
            GridLayout newLayout = new GridLayout(allPlayerList.size(),1);
            dbPlayerPane.setLayout(newLayout);
            choosenPlayerList.clear();
            
            for(Player player : allPlayerList){
                ChoosePlayerPanel textLabel = new ChoosePlayerPanel(player);
                dbPlayerPane.add(textLabel);
                choosenPlayerList.add(textLabel);
            }
            
            
            
            try {
                connectionSource.close();
            } catch (IOException ex) {
                Logger.getLogger(TournamentApp.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(TournamentApp.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    private void prepareList(){
        try {
            Class.forName("org.sqlite.JDBC");
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(TournamentApp.class.getName()).log(Level.SEVERE, null, ex);
        }
        ConnectionSource connectionSource;
        try {
            connectionSource = new JdbcConnectionSource(TournamentApp.DB_NAME);
            Dao<Player, String> playerDao = DaoManager.createDao(connectionSource, Player.class);
            
            QueryBuilder<Player, String> queryBuilder = playerDao.queryBuilder();
            Where<Player, String> where = queryBuilder.where();
            if(!NameField.getText().isEmpty()){
                where.eq("firstName", NameField.getText());
                if(!(LastName.getText().isEmpty() && NickName.getText().isEmpty() && City.getText().isEmpty())){
                    where.and();
                }
            }
            if(!LastName.getText().isEmpty()){
                where.eq("lastName", LastName.getText());
                if(!(NickName.getText().isEmpty() && City.getText().isEmpty())){
                    where.and();
                }
                
            }
            if(!NickName.getText().isEmpty()){
                where.eq("nickName", NickName.getText());
                if(!(City.getText().isEmpty())){
                    where.and();
                }
            }
            if(!City.getText().isEmpty()){
                where.eq("City", City.getText());

            }           
            
            PreparedQuery<Player> preparedQuery = queryBuilder.prepare();
            List<Player> allPlayerList = playerDao.query(preparedQuery);
            
            GridLayout newLayout = new GridLayout(allPlayerList.size(),1);
            dbPlayerPane.setLayout(newLayout);
            
            choosenPlayerList.clear();
            
            for(Player player : allPlayerList){
                ChoosePlayerPanel textLabel = new ChoosePlayerPanel(player);
                dbPlayerPane.add(textLabel);
                choosenPlayerList.add(textLabel);
               
            }
            
            try {
                connectionSource.close();
            } catch (IOException ex) {
                Logger.getLogger(TournamentApp.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(TournamentApp.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
